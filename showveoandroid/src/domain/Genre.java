package domain;

/**
 * A DTO for genre information.
 */
public class Genre {

	private String _id;
	private String _name;

	public String getID() { return _id; }
	public void setID(String value) { _id = value; }

	public String getName() { return _name; }
	public void setName(String value) { _name = value; }

}
